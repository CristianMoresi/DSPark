    // ------------------------------------------------------------------------
    // Huffman table self-validation (regression guard).
    //
    // Every ISO 11172-3 table must be a *complete prefix code*: kraftComplete()
    // proves the Kraft equality (sum of 2^-len == 1) with exact integer math, so
    // any transcription error that alters a length or adds/drops an entry breaks
    // the build. prefixFree() proves no codeword is a prefix of another (catches
    // same-length value typos and duplicates). Both are constexpr; the static
    // assertions below run them at compile time over all 15 tables.
    // ------------------------------------------------------------------------
    template <std::size_t N>
    static constexpr bool kraftComplete(const HuffCode (&t)[N]) noexcept
    {
        int maxLen = 0;
        for (std::size_t i = 0; i < N; ++i)
            if (t[i].len > maxLen) maxLen = t[i].len;
        const unsigned long long full = 1ull << maxLen;
        unsigned long long sum = 0;
        for (std::size_t i = 0; i < N; ++i)
            sum += full >> t[i].len;     // 2^(maxLen-len), exact
        return sum == full;              // == 2^maxLen  <=>  Kraft sum == 1
    }

    template <std::size_t N>
    static constexpr bool prefixFree(const HuffCode (&t)[N]) noexcept
    {
        for (std::size_t i = 0; i < N; ++i)
            for (std::size_t j = 0; j < N; ++j)
                if (i != j && t[i].len <= t[j].len &&
                    t[i].code == static_cast<uint16_t>(t[j].code >> (t[j].len - t[i].len)))
                    return false;        // codeword i is a prefix of codeword j
        return true;
    }

