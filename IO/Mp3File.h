    // -- AudioFile interface ---------------------------------------------------

    [[nodiscard]] bool openRead(const std::filesystem::path& path) override
    {
        close();

        std::ifstream in(path, std::ios::binary | std::ios::ate);
        if (!in.is_open()) return false;

        auto fileSize = static_cast<size_t>(in.tellg());
        if (fileSize < 10) return false;
        
        constexpr size_t kMaxMp3FileSize = 256 * 1024 * 1024;  // 256 MB bounds check
        if (fileSize > kMaxMp3FileSize) return false;
        in.seekg(0, std::ios::beg);

        fileData_.resize(fileSize);
        in.read(reinterpret_cast<char*>(fileData_.data()), static_cast<std::streamsize>(fileSize));
        if (static_cast<size_t>(in.gcount()) != fileSize)
        {
            fileData_.clear();
            return false;
        }
        in.close();

        filePos_ = 0;
        skipID3v2();

        if (!scanFrames())
        {
            fileData_.clear();
            return false;
        }

        if (!decodeAll())
        {
            fileData_.clear();
            decodedSamplesFlat_.clear();
            return false;
        }

        fileData_.clear(); // Free raw memory after full planar extraction
        isOpen_ = true;
        return true;
    }

    [[nodiscard]] bool openWrite(const std::filesystem::path& path, const AudioFileInfo& info) override
    {
        close();
        outFile_.open(path, std::ios::binary | std::ios::trunc);
        if (!outFile_.is_open()) return false;

        info_ = info;
        if (info_.numChannels < 1) info_.numChannels = 1;
        if (info_.numChannels > 2) info_.numChannels = 2;

        encBitrate_ = info_.bitsPerSample;
        if (encBitrate_ < 32) encBitrate_ = 128;
        
        static constexpr int validBr[] = {32,40,48,56,64,80,96,112,128,160,192,224,256,320};
        int best = 128, bestDist = 999;
        for (int br : validBr) {
            int d = std::abs(br - encBitrate_);
            if (d < bestDist) { bestDist = d; best = br; }
        }
        encBitrate_ = best;

        double sr = info_.sampleRate;
        if (sr <= 36050) info_.sampleRate = 32000;
        else if (sr <= 46050) info_.sampleRate = 44100;
        else info_.sampleRate = 48000;

        for (int ch = 0; ch < kChannelsMax; ++ch) encState_[ch] = {};
        encFrameBuf_.clear();
        encInputPos_ = 0;
        isWriting_ = true;
        isOpen_ = true;
        return true;
    }

    [[nodiscard]] AudioFileInfo getInfo() const override { return info_; }

    [[nodiscard]] bool readSamples(AudioBufferView<float> dest) override
    {
        return readSamples(dest, 0, info_.numSamples);
    }

    [[nodiscard]] bool readSamples(AudioBufferView<float> dest,
                                   int64_t startFrame, int64_t numFrames) override
    {
        if (!isOpen_) return false;
        if (startFrame < 0 || numFrames <= 0) return false;
        if (startFrame + numFrames > info_.numSamples) return false;

        const int nCh = std::min(dest.getNumChannels(), static_cast<int>(info_.numChannels));
        const int64_t toCopy = std::min(numFrames, static_cast<int64_t>(dest.getNumSamples()));
        const size_t bytesToCopy = static_cast<size_t>(toCopy) * sizeof(float);

        for (int ch = 0; ch < nCh; ++ch)
        {
            float* dst = dest.getChannel(ch);
            const float* src = decodedSamplesFlat_.data() + static_cast<size_t>(ch * info_.numSamples + startFrame);
            std::memcpy(dst, src, bytesToCopy);
        }

        return true;
    }

    [[nodiscard]] bool writeSamples(AudioBufferView<const float> src) override
    {
        if (!isWriting_ || !outFile_.is_open()) return false;

        const int nCh = std::min(src.getNumChannels(), static_cast<int>(info_.numChannels));
        const int nS = src.getNumSamples();

        for (int i = 0; i < nS; ++i)
        {
            for (int ch = 0; ch < static_cast<int>(info_.numChannels); ++ch)
            {
                float val = (ch < nCh) ? src.getChannel(ch)[i] : 0.0f;
                encInput_[ch][encInputPos_] = static_cast<double>(val);
            }
            ++encInputPos_;

            if (encInputPos_ >= kSamplesPerFrame)
            {
                encEncodeFrame();
                encInputPos_ = 0;
            }
        }
        return true;
    }

    void close() override
    {
        if (isWriting_ && outFile_.is_open())
        {
            if (encInputPos_ > 0)
            {
                for (int ch = 0; ch < static_cast<int>(info_.numChannels); ++ch)
                    for (int i = encInputPos_; i < kSamplesPerFrame; ++i)
                        encInput_[ch][i] = 0.0;
                encEncodeFrame();
            }
            outFile_.close();
        }
        isWriting_ = false;

        fileData_.clear();
        decodedSamplesFlat_.clear();
        frameOffsets_.clear();
        info_ = {};
        isOpen_ = false;
        filePos_ = 0;
    }

    [[nodiscard]] bool isOpen() const noexcept override { return isOpen_; }

